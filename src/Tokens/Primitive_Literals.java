package Tokens;

public class Primitive_Literals {
public static void main(String[] args) {
	System.out.println(20);//integer literal
	System.out.println(2.0);//Decimal literal
	System.out.println('A');//character literal
	System.out.println(true);// Boolean literal
	
	System.out.println("vijay kumar");//String literal
}

}
